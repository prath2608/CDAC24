/*
	i. Experiment with converting a byte value into other 
	primitive types or vice versa and observe the results.
*/

class ToWider{
	public static void main(String[] args){
		
		byte a = 10;
		int x = (int)a;
		System.out.println(x);
		
		int b = 125;
		byte y = (byte)b;
		System.out.println(y);
		
		
	}
}